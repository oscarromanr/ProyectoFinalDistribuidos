package entidades;

/**
 *
 * @author roman
 */
public class Sensor {
    private Long id;
    private String nombre;
    private String marca;

    public Sensor() {
    }

    public Sensor(String nombre, String marca) {
        this.nombre = nombre;
        this.marca = marca;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    @Override
    public String toString() {
        return "Sensor{" + "id=" + id + ", nombre=" + nombre + ", marca=" + marca + '}';
    }

}